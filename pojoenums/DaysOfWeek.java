package pojoenums;

/*
    Enum is a java class,
    All methods are static final
    Constructor is private (cannot initialize it)
    Cannot extend any class (by default extends java.lang.Enum)
    Can implement any interface
    Can have abstract methods
    Other classes cannot extend enums.
 */
public enum DaysOfWeek {
    MONDAY,
    TUESDAY,
    WEDNESDAY,
    THURSDAY,
    FRIDAY,
    SATURDAY,
    SUNDAY;
}
